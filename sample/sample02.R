# 数値と文字列


# 数値 + = * / %/% %% ^
5 + 3 # たしざん
10 / 3 # わりざん
10 %/% 3 # 商の整数部分
10 %% 3 # あまり
2 ^ 4 # べき乗
cos(1) # 正弦
sqrt(2) # 平方根
round(2.5555,2) # 四捨五入。コンマの後ろが小数点以下の桁数。

# 文字列 ""で囲む
paste("a", "b", "c") # abcをつなげる
paste("a", "b", "c", sep="")
paste("a", "b", "c", sep="/")

# 数値と文字列の相互変換
x <- 5
as.character(x) # 数値を文字列に変換
s <- "5"
as.numeric(s) # 文字列を数値に変換
